export function AlertFeed({
  alerts,
  totalCount,
  severityFilter,
  onFilterChange,
  onAlertSelect,
  selectedAlertId,
}: AlertFeedProps) {
  const filterOptions: Array<{ value: typeof severityFilter; count: number }> = [
    { value: "all", count: totalCount },
    { value: "critical", count: alerts.filter((a) => a.severity === "critical").length },
    { value: "high", count: alerts.filter((a) => a.severity === "high").length },
    { value: "medium", count: alerts.filter((a) => a.severity === "medium").length },
    { value: "low", count: alerts.filter((a) => a.severity === "low").length },
  ];

  return (
    <div className="rounded-xl border border-border bg-surface-1">
      {/* Header */}
      <div className="border-b border-border p-4">
        <h3 className="font-medium text-text-primary">Alerts</h3>
        <p className="text-xs text-text-secondary">
          {totalCount} active alerts
        </p>
      </div>

      {/* Filter tabs */}
      <div className="flex flex-wrap gap-1 border-b border-border p-2">
        {filterOptions.map((option) => {
          const isActive = severityFilter === option.value;
          const config = option.value !== "all" ? SEVERITY_CONFIG[option.value] : null;

          return (
            <button
              key={option.value}
              onClick={() => onFilterChange(option.value)}
              className={`rounded-md px-2 py-1 text-xs font-medium transition-colors ${
                isActive
                  ? config
                    ? `${config.className} border`
                    : "bg-brand text-white"
                  : "bg-surface-2 text-text-secondary hover:bg-surface-3"
              }`}
            >
              {option.value.charAt(0).toUpperCase() + option.value.slice(1)}
              {option.count > 0 && (
                <span className="ml-1 opacity-70">({option.count})</span>
              )}
            </button>
          );
        })}
      </div>

      {/* Alert list */}
      <div className="max-h-[600px] overflow-y-auto">
        {alerts.length === 0 ? (
          <div className="p-8 text-center">
            <svg
              className="mx-auto h-12 w-12 text-text-muted"
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={1.5}
                d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z"
              />
            </svg>
            <p className="mt-2 text-sm text-text-secondary">No alerts</p>
            <p className="text-xs text-text-muted">All systems operational</p>
          </div>
        ) : (
          <div className="divide-y divide-border">
            {alerts.map((alert) => {
              const config = SEVERITY_CONFIG[alert.severity];
              const isSelected = selectedAlertId === alert.id;
              const typeIcon = ALERT_TYPE_ICONS[alert.type] || config.icon;

              return (
                <button
                  key={alert.id}
                  onClick={() => onAlertSelect(isSelected ? null : alert.id)}
                  className={`w-full p-3 text-left transition-colors hover:bg-surface-2/50 ${
                    isSelected ? "bg-surface-2" : ""
                  }`}
                >
                  <div className="flex items-start gap-3">
                    <span className={`mt-0.5 flex-shrink-0 ${config.className.split(" ")[1]}`}>
                      {typeIcon}
                    </span>
                    <div className="min-w-0 flex-1">
                      <div className="flex items-center gap-2">
                        <span className={`inline-flex items-center rounded-full px-1.5 py-0.5 text-[10px] font-medium ${config.className}`}>
                          {config.label}
                        </span>
                        {alert.schoolName && (
                          <span className="text-[10px] text-text-muted">
                            {alert.schoolName}
                          </span>
                        )}
                      </div>
                      <p className="mt-1 text-sm font-medium text-text-primary line-clamp-1">
                        {alert.title}
                      </p>
                      <p className="mt-0.5 text-xs text-text-secondary line-clamp-2">
                        {alert.description}
                      </p>
                      <p className="mt-1 text-[10px] text-text-muted">
                        {formatRelativeTime(alert.createdAt)}
                      </p>
                    </div>
                  </div>
                </button>
              );
            })}
          </div>
        )}
      </div>
    </div>
  );
}
